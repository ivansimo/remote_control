    anomalias = models.CharField(max_length=200, blank=True)
    horas_funcionamiento = models.CharField(max_length=200, blank=True)
    motor_modelo = models.CharField(max_length=200, blank=True)
    motor_n_serie = models.CharField(max_length=200, blank=True)
    alternador_modelo = models.CharField(max_length=200, blank=True)
