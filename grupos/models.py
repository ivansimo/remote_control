    filtros_aire_cant = models.CharField(max_length=200, blank=True)
    filtros_aire_modelo = models.CharField(max_length=200, blank=True)
    otros_respuestos = models.CharField(max_length=800, blank=True)
    descripción = models.TextField(blank=True)
    foto_principal = models.ImageField(upload_to='photos/%Y/%m/%d/')
    foto_equipo_control_1 = models.ImageField(upload_to='photos/%Y/%m/%d/')
    foto_equipo_control_2 = models.ImageField(upload_to='photos/%Y/%m/%d/', blank=True)
    foto_motor_1 = models.ImageField(upload_to='photos/%Y/%m/%d/')
    foto_motor_2 = models.ImageField(upload_to='photos/%Y/%m/%d/', blank=True)
    foto_alternador_1 = models.ImageField(upload_to='photos/%Y/%m/%d/')
    foto_alternador_2 = models.ImageField(upload_to='photos/%Y/%m/%d/', blank=True)
    photo_8 = models.ImageField(upload_to='photos/%Y/%m/%d/', blank=True)
